public class Kalkulator {

    int a;
    int b;

    public Kalkulator(int a, int b) {
        this.a = a;
        this.b = b;
    }

    // Penjumlahan
    public int hitung_Penjumlahan(){ return ( this.a + this.b);}

    // Pengurangan
    public int hitung_Pengurangan(){ return ( this.a - this.b);}

    // Perkalian
    public int hitung_Perkalian(){ return ( this.a * this.b);}

    // Pembagian
    public int hitung_Pembagian(){ return ( this.a / this.b);}





}
